const optText=o=>(o||'').replace(/^[A-E][、,，.．]\s*/,'');
const pick=(a,s)=>a[s%a.length];

function findRule(stem){for(const r of RULES){if(r.re.test(stem))return r;}return null;}
function findTopic(stem){for(const[re,t,reg,deep]of TOPICS){if(re.test(stem))return{kp:reg+'中关于'+t+'的规定',deep:deep,pit:''};}return{kp:FB_REG+'中的'+FB_KP,deep:FB_DEEP,pit:''};}

function build(q){
  const stem=q.stem||'';
  const seed=stem.length+(stem.charCodeAt(0)||0)+(stem.charCodeAt(stem.length-1)||0)+(stem.charCodeAt(stem.length>>1)||0);
  const rule=findRule(stem)||findTopic(stem);
  const neg=/不属于|不正确|不符合|错误的|不包括|不应当|不得作为/.test(stem);
  const nums=(stem.match(/\d+(?:\.\d+)?\s*(?:人|米|mm|cm|m|天|日|小时|%|‰|万元|元|kg|t|级|年|次|倍|层|℃)/g)||[]).slice(0,3);
  const numStr=nums.join('、');
  const orig=(q.explanation||'').trim();
  const keepOrig=orig&&!GENERIC.test(orig)&&!/【核心考点】/.test(orig)?orig:'';
  let concl='',pitLine='';
  if(q.type==='判断'){
    if(q.answer==='A'){
      concl=pick(['题干这句话与上述要求一致，判对。','题干说的正是这个规矩，放心判对。','对照上面的要求，题干原话没毛病，判对。'],seed);
      if(nums.length)concl+='题干中「'+numStr+'」即为规定数值，这个数就是记忆点。';
    }else{
      concl=pick(['题干与上述要求不符，判错。','拿上面的规矩一对照，题干的说法站不住脚，判错。','题干跟规定拧着来，判错。'],seed);
      pitLine=nums.length?'本题最可能动手脚的位置就是「'+numStr+'」这类数值和"应当/可以、不得/严禁"的措辞，对照正确说法找出被改动的点。':(rule.pit||'');
    }
  }else if(q.type==='单选'){
    const ci=q.answer.charCodeAt(0)-65;
    const txt=optText(q.options[ci]);
    const others=q.options.map((o,i)=>i===ci?null:optText(o)).filter(Boolean);
    const shortOpts=others.length&&others.every(o=>o.length<=10);
    if(neg){
      concl=pick(['本题是反向提问（选不符合项）：应选 '+q.answer+'（'+txt+'），它与上述要求不符，其余选项均为正确做法。','注意题干的否定词——'+q.answer+'（'+txt+'）恰恰违背上述要求，正是要选的那项。'],seed);
      pitLine='反向题最大的坑是惯性思维：看到顺眼的正确说法就想选，务必先圈出题干里的"不"字。';
    }else{
      concl=pick(['正确答案 '+q.answer+'：'+txt+'。','应选 '+q.answer+'（'+txt+'），这与上述要求完全对应。','答案 '+q.answer+'——'+txt+'。'],seed);
      if(shortOpts)pitLine='「'+others.join('」「')+'」'+pick(['是拿相邻数值或相近说法凑出来的干扰项，与上述规定对不上。','看着眼熟，但都不是规定的那个值/说法，逐一对照即可排除。'],seed)+(rule.pit?rule.pit:'');
      else pitLine=rule.pit||'';
    }
  }else{
    const letters=q.answer.split('').join('、');
    const corrects=q.answer.split('').map(L=>({L:L,t:optText(q.options[L.charCodeAt(0)-65]||'')}));
    const canList=corrects.length<=4&&corrects.every(c=>c.t&&c.t.length<=16);
    if(neg){
      concl='本题为反向多选：'+letters+' 均与上述要求不符，正是要选的；其余选项为正确做法。';
      pitLine='注意题干否定词，别把正确做法选进来。';
    }else{
      concl=canList?('正确答案 '+letters+'：'+corrects.map(c=>c.L+'（'+c.t+'）').join('、')+'，均为上述规定的明确要求。'):pick(['正确答案 '+letters+'，各项均属上述规定的明确要求。','应选 '+letters+'——每一项都能在上述要求中找到对应。'],seed);
      pitLine=(rule.pit?rule.pit:'')+'多选题少选、错选均不得分，把每个选项当判断题逐个过一遍再提交。';
    }
  }
  let html='<b>【核心考点】</b>'+rule.kp+'。';
  html+='<br><b>【深度解析】</b>'+rule.deep+(keepOrig?' '+keepOrig:'')+' '+concl;
  if(pitLine)html+='<br><b>【错项辨析】</b>'+pitLine;
  return html;
}

// ============ 三、标准JSON读写 ============
let ruleHit=0,topicHit=0,total=0;
for(const cat of ['A','B','C1','C2','C3']){
  const file=__dirname+'/questions_'+cat+'.json';
  const qs=JSON.parse(fs.readFileSync(file,'utf8'));
  for(const q of qs){
    total++;
    if(findRule(q.stem))ruleHit++;else topicHit++;
    q.explanation=build(q);
  }
  const out=JSON.stringify(qs);
  JSON.parse(out); // 写盘前自校验
  fs.writeFileSync(file,out);
  console.log(cat+': '+qs.length+'题 已写入');
}
console.log('总计'+total+'题, 硬考点命中'+ruleHit+' ('+Math.round(ruleHit/total*100)+'%), 兜底考点'+topicHit);
